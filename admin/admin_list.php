<?php
// 后台相册管理与列表 - 动态目录布局优化与批量删除版
ini_set('display_errors', 1);
ini_set('display_startup_errors', 1);
error_reporting(E_ALL);

if (file_exists("./ini.php")) {
    include("./ini.php"); 
}

session_start();

$page_title="图片管理";
// ===================================================
// 1. 扫描 ../user/ 下所有子目录，读取其 ini.php
// ===================================================
$userBaseDir = '../user/';
$albums = []; 

if (is_dir($userBaseDir)) {
    $subDirs = scandir($userBaseDir);
    foreach ($subDirs as $dir) {
        if ($dir === '.' || $dir === '..') continue;
        
        $currentPath = $userBaseDir . $dir;
        if (is_dir($currentPath)) {
            $albumIniPath = $currentPath . '/ini.php';
            if (file_exists($albumIniPath)) {
                // 使用闭包隔离作用域方式安全读取子目录的 $config
                $albumConfig = call_user_func(function($path) {
                    @include($path);
                    return isset($config) ? $config : null;
                }, $albumIniPath);
                
                if ($albumConfig && isset($albumConfig['title'])) {
                    $albums[$dir] = [
                        'title' => $albumConfig['title'],
                        'config' => $albumConfig,
                        'file_path' => $albumIniPath // 记录文件真实路径，方便后续保存写入
                    ];
                }
            }
        }
    }
}

// 决定当前选中的相册目录
$selectedAlbumKey = isset($_POST['target_album']) ? $_POST['target_album'] : (isset($_GET['target_album']) ? $_GET['target_album'] : '');
if (empty($selectedAlbumKey) && !empty($albums)) {
    $selectedAlbumKey = array_key_first($albums);
}

// 动态锁定当前的相册配置和文件路径
$currentAlbumConfig = isset($albums[$selectedAlbumKey]) ? $albums[$selectedAlbumKey]['config'] : null;
$currentAlbumIniPath = isset($albums[$selectedAlbumKey]) ? $albums[$selectedAlbumKey]['file_path'] : null;

// ===================================================
// 2. 鉴权逻辑
// ===================================================
if ($currentAlbumConfig) {
    $logged_in_key = isset($currentAlbumConfig['login_admin']) ? $currentAlbumConfig['login_admin'] : 'logged_in_admin';
    if (!(isset($_SESSION['logged_in_admin']) && $_SESSION['logged_in_admin'] === true) && (empty($_SESSION[$logged_in_key]) || $_SESSION[$logged_in_key] !== true)) {
        header('Location: login.php');
        exit();
    }
} else {
    if (!(isset($_SESSION['logged_in_admin']) && $_SESSION['logged_in_admin'] === true)) {
        header('Location: login.php');
        exit();
    }
}

$_SESSION['admin_logged_in'] = true;

// ===================================================
// 3. 动态设置与路径自动换算（核心：解决群晖 open_basedir 越界）
// ===================================================
$uploadDir          = $currentAlbumConfig ? $currentAlbumConfig['tu_1'] : '';
$thumbnailDir       = $currentAlbumConfig ? $currentAlbumConfig['min'] : ''; 
$largeThumbnailDir  = $currentAlbumConfig ? $currentAlbumConfig['max'] : '';

// 将配置中的 ../../ 动态转换为适应 admin/ 目录的 ../
if ($uploadDir && substr($uploadDir, 0, 6) === '../../') {
    $uploadDir = '../' . substr($uploadDir, 6);
}
if ($thumbnailDir && substr($thumbnailDir, 0, 6) === '../../') {
    $thumbnailDir = '../' . substr($thumbnailDir, 6);
}
if ($largeThumbnailDir && substr($largeThumbnailDir, 0, 6) === '../../') {
    $largeThumbnailDir = '../' . substr($largeThumbnailDir, 6);
}

// ===================================================
// 4. 处理单项与批量删除及表单保存逻辑
// ===================================================
$saveMessage = '';
$saveMessageType = 'success';

// 处理表单保存修改（更新对应相册的 ini.php）
if ($_SERVER["REQUEST_METHOD"] == "POST" && isset($_POST['save_config'])) {
    if (empty($currentAlbumIniPath) || !file_exists($currentAlbumIniPath)) {
        $saveMessage = "无法保存：未找到对应的相册配置文件。";
        $saveMessageType = 'error';
    } else {
        $newConfig = $currentAlbumConfig; // 继承旧数据，防止漏掉其他未在表单展现的参数
        
        $newConfig['title']       = isset($_POST['title']) ? trim($_POST['title']) : '';
        $newConfig['login_admin'] = isset($_POST['login_admin']) ? trim($_POST['login_admin']) : 'login_default';
        $newConfig['list_read']   = isset($_POST['list_read']) ? $_POST['list_read'] : '1';
        $newConfig['list_home']   = isset($_POST['list_home']) ? $_POST['list_home'] : '0';
        $newConfig['cover']       = isset($_POST['cover']) ? trim($_POST['cover']) : '';

        $configContent = "<?php\n";
        $configContent .= "// This file is automatically generated by the admin panel.\n";
        $configContent .= "// DO NOT EDIT MANUALLY unless you know what you are doing.\n";
        $configContent .= "\$config = " . var_export($newConfig, true) . ";\n";
        $configContent .= "?>";

        if (@file_put_contents($currentAlbumIniPath, $configContent) !== false) {
            $saveMessage = "相册【" . $newConfig['title'] . "】配置更新成功！";
            $currentAlbumConfig = $newConfig;
            if (isset($albums[$selectedAlbumKey])) {
                $albums[$selectedAlbumKey]['title'] = $newConfig['title'];
                $albums[$selectedAlbumKey]['config'] = $newConfig;
            }
        } else {
            $saveMessage = "保存失败，请检查文件权限：" . $currentAlbumIniPath;
            $saveMessageType = 'error';
        }
    }
}

// 处理单张图片删除
if ($_SERVER["REQUEST_METHOD"] == "POST" && isset($_POST['delete_image'])) {
    $imgName = isset($_POST['image_name']) ? basename($_POST['image_name']) : '';
    if ($imgName && !empty($uploadDir)) {
        $targetImg = $uploadDir . $imgName;
        $targetMin = $thumbnailDir . 'min_' . $imgName;
        $targetMax = $largeThumbnailDir . 'max_' . $imgName;

        if (@unlink($targetImg)) {
            @unlink($targetMin);
            @unlink($targetMax);
            $saveMessage = "图片 $imgName 已成功删除。";
        } else {
            $saveMessage = "图片删除失败，请检查权限。";
            $saveMessageType = 'error';
        }
    }
}

// 处理批量删除图片
if ($_SERVER["REQUEST_METHOD"] == "POST" && isset($_POST['batch_delete'])) {
    $selectedImages = isset($_POST['images']) ? $_POST['images'] : [];
    if (!empty($selectedImages) && !empty($uploadDir)) {
        $successCount = 0;
        foreach ($selectedImages as $imgName) {
            $imgName = basename($imgName); // 安全过滤
            $targetImg = $uploadDir . $imgName;
            $targetMin = $thumbnailDir . 'min_' . $imgName;
            $targetMax = $largeThumbnailDir . 'max_' . $imgName;

            if (@unlink($targetImg)) {
                @unlink($targetMin);
                @unlink($targetMax);
                $successCount++;
            }
        }
        $saveMessage = "成功批量删除 {$successCount} 张图片（含其缩略图）。";
    } else {
        $saveMessage = "未选中任何图片或路径无效。";
        $saveMessageType = 'error';
    }
}

// 获取换算后的相册图片列表
$images = [];
if (!empty($uploadDir) && is_dir($uploadDir)) {
    $files = scandir($uploadDir);
    if ($files !== false) {
        foreach ($files as $file) {
            if ($file === '.' || $file === '..') continue;
            $ext = strtolower(pathinfo($file, PATHINFO_EXTENSION));
            if (in_array($ext, ['jpg', 'jpeg', 'png', 'gif', 'webp', 'svg', 'ico'])) {
                $images[] = $file;
            }
        }
    }
}
