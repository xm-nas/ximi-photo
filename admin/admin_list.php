    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const selectAllCheckbox = document.getElementById('selectAllCheckbox');
            const imgSelectors = document.querySelectorAll('.img-selector');
            const selectCountSpan = document.getElementById('selectCount');
            const batchDeleteBtn = document.getElementById('batchDeleteBtn');
            const batchDeleteForm = document.getElementById('batchDeleteForm');

            if (!selectAllCheckbox) return;

            // 更新选中计数器
            function updateSelectCount() {
                const checkedCount = document.querySelectorAll('.img-selector:checked').length;
                selectCountSpan.textContent = checkedCount;
            }

            // “全选/取消全选” 复选框事件
            selectAllCheckbox.addEventListener('change', function () {
                imgSelectors.forEach(checkbox => {
                    checkbox.checked = selectAllCheckbox.checked;
                });
                updateSelectCount();
            });

            // 监听每个单项复选框的点击
            imgSelectors.forEach(checkbox => {
                checkbox.addEventListener('change', function () {
                    // 如果有一个没选，全选框取消勾选
                    if (!this.checked) {
                        selectAllCheckbox.checked = false;
                    } else {
                        // 如果所有人都勾选了，全选框自动勾选
                        const allChecked = document.querySelectorAll('.img-selector:checked').length === imgSelectors.length;
                        selectAllCheckbox.checked = allChecked;
                    }
                    updateSelectCount();
                });
            });

            // 批量删除按钮触发（SweetAlert2 提示）
            batchDeleteBtn.addEventListener('click', function () {
                const checkedCount = document.querySelectorAll('.img-selector:checked').length;
                if (checkedCount === 0) {
                    Swal.fire({
                        title: '提示',
                        text: '请先勾选需要删除的图片！',
                        icon: 'warning',
                        confirmButtonText: '我知道了'
                    });
                    return;
                }

                Swal.fire({
                    title: '确认批量删除？',
                    text: `您已选中了 ${checkedCount} 张图片，删除后文件及缩略图将永久消失且无法恢复！`,
                    icon: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#dc3545',
                    cancelButtonColor: '#6c757d',
                    confirmButtonText: '确定永久删除',
                    cancelButtonText: '取消'
                }).then((result) => {
                    if (result.isConfirmed) {
                        batchDeleteForm.submit();
                    }
                });
            });
        });

        // 单项删除函数
        function confirmSingleDelete(imgName) {
            Swal.fire({
                title: '确认删除？',
                text: `确定要永久删除图片 "${imgName}" 吗？（对应的缩略图也将被同步清理）`,
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#dc3545',
                cancelButtonColor: '#6c757d',
                confirmButtonText: '确定删除',
                cancelButtonText: '取消'
            }).then((result) => {
                if (result.isConfirmed) {
                    document.getElementById('singleDeleteImgName').value = imgName;
                    document.getElementById('singleDeleteForm').submit();
                }
            });
        }
